    public void displayFoodCatalogue() {
        DefaultTableModel tablemodel = (DefaultTableModel) tblMMCatalogue.getModel();
        tablemodel.setRowCount(0);
        for (Restaurant restro:ecosystem.getRestaurantDirectory().getRestaurantDirectory()) {
            if (restro.getUserName().equals(account.getUsername())) {
                System.out.println("1");
                if(!(restro.getMenu().isEmpty())){
                    for(Dishlist menu:restro.getMenu()){
                        System.out.println("2");
                        Object[] row = new Object[3];
                        row[0] = menu.getName();
                        row[1] = menu.getDetails();
                        row[2] = menu.getOrderAmount();
                        tablemodel.addRow(row);
                    }
                }
            }
        }
    }
